
// Logical Operators: AND, OR, NOT

let myFavoriteNumber = 7;
// AND
console.log(myFavoriteNumber > 0 && myFavoriteNumber <= 9)
console.log(myFavoriteNumber > 0 && myFavoriteNumber <= 5)

// OR
console.log(myFavoriteNumber > 0 || myFavoriteNumber <= 9)
console.log(myFavoriteNumber == 1 || myFavoriteNumber <= 5)

// NOT!
console.log(!(myFavoriteNumber == 1))
console.log(!(myFavoriteNumber == 1 || myFavoriteNumber <= 5))
